/**
 * Use a loop to count down through a set of values
 *
 * @author CMS 167
 */
 
public class Countdown {
 
    public static void main(String[] args) {
     
        int n = 5;
      
        while (n > 0) {
            System.out.println(n);
            n = n - 1;
        }
     
        System.out.println("Blastoff!");
     
    }
 
}
